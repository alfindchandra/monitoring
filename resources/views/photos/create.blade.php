<script>
// Album selector helper
const albumSelect = document.getElementById('album_select');
const albumInput = document.getElementById('album_input');

if (albumSelect) {
    albumSelect.addEventListener('change', function() {
        if(this.value) {
            albumInput.value = this.value;
        }
    });
