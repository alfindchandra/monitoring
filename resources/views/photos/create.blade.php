// File preview logic
const photoInput = document.getElementById('photos');
const previewArea = document.getElementById('preview');
const previewContainer = document.getElementById('preview-container');
const defaultTitleInput = document.getElementById('default_title');

photoInput.addEventListener('change', function(e) {
    const files = Array.from(e.target.files);
    
    if (files.length === 0) {
        previewArea.classList.add('hidden');
        return;
    }

    previewArea.classList.remove('hidden');
    previewContainer.innerHTML = '';
    
    // Ambil nilai default title saat ini (jika ada)
    const currentDefaultTitle = defaultTitleInput.value;

    files.forEach((file, index) => {
        const reader = new FileReader();
        
        reader.onload = function(e) {
            const div = document.createElement('div');
            div.className = 'bg-gray-50 rounded-lg p-4 flex gap-4 border border-gray-200 items-start';
            
            // Bersihkan nama file dari ekstensi untuk saran judul
            const fileNameClean = file.name.replace(/\.[^/.]+$/, "");
            
            div.innerHTML = `
                <div class="flex-shrink-0">
                    <img src="${e.target.result}" class="w-24 h-24 object-cover rounded shadow-sm">
                    <p class="text-xs text-gray-500 mt-2 text-center">${(file.size / 1024 / 1024).toFixed(2)} MB</p>
                </div>
                <div class="flex-1">
                    <div class="mb-3">
                        <label class="block text-xs text-gray-500 mb-1">Judul Foto</label>
                        <input type="text" 
                               name="titles[]" 
                               placeholder="Judul khusus foto ini (kosongkan untuk pakai default)"
                               class="w-full border border-gray-300 rounded px-3 py-2 text-sm focus:ring-1 focus:ring-blue-500 focus:border-blue-500"
                               value=""> 
                    </div>
                    <div>
                        <label class="block text-xs text-gray-500 mb-1">Deskripsi</label>
                        <textarea name="descriptions[]" 
                                  placeholder="Deskripsi khusus foto ini (opsional)"
                                  rows="2"
                                  class="w-full border border-gray-300 rounded px-3 py-2 text-sm focus:ring-1 focus:ring-blue-500 focus:border-blue-500"></textarea>
                    </div>
                </div>
            `;
            previewContainer.appendChild(div);
        };
        
        reader.readAsDataURL(file);
    });
});

// Upload Form Submission handling (Loading State)
document.getElementById('uploadForm').addEventListener('submit', function() {
    const btn = document.getElementById('submitBtn');
    btn.disabled = true;
    btn.innerHTML = '<i class="fas fa-spinner fa-spin mr-2"></i> Mengunggah...';
    btn.classList.add('opacity-75', 'cursor-not-allowed');
});

// Drag & drop Visuals
const dropArea = document.getElementById('drop-area');

['dragenter', 'dragover', 'dragleave', 'drop'].forEach(eventName => {
    dropArea.addEventListener(eventName, preventDefaults, false);
});

function preventDefaults(e) {
    e.preventDefault();
    e.stopPropagation();
}

['dragenter', 'dragover'].forEach(eventName => {
    dropArea.addEventListener(eventName, () => {
        dropArea.classList.add('border-blue-500', 'bg-blue-50');
    });
});

['dragleave', 'drop'].forEach(eventName => {
    dropArea.addEventListener(eventName, () => {
        dropArea.classList.remove('border-blue-500', 'bg-blue-50');
    });
});

dropArea.addEventListener('drop', function(e) {
    const dt = e.dataTransfer;
    const files = dt.files;
    photoInput.files = files;
    photoInput.dispatchEvent(new Event('change'));
});
</script>
